function slugifyCategory(name: string) {
  const matchedCategory = categoryDefinitions.find((category) => category.name === name);

  if (matchedCategory) {
    return matchedCategory.slug;
  }

  return name
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "-");
}

function makeAnchor(text: string, index: number) {
  const cleaned = text
    .trim()
    .replace(/^[0-9٠-٩]+[\-.\s]*/, "")
    .slice(0, 50);

  return (
    cleaned
      .toLowerCase()
      .replace(/\s+/g, "-")
      .replace(/[^\u0600-\u06FFa-z0-9\-]/gi, "")
      .replace(/\-+/g, "-")
      .replace(/^\-|\-$/g, "") || `section-${index + 1}`
  );
}

async function main() {
  const prisma = createPrismaClient();

  try {
    for (const categoryDefinition of categoryDefinitions) {
      await prisma.category.upsert({
        where: { name: categoryDefinition.name },
        update: {
          slug: categoryDefinition.slug,
        },
        create: {
          name: categoryDefinition.name,
          slug: categoryDefinition.slug,
        },
      });
    }

    for (const article of seedArticles) {
      const category = await prisma.category.upsert({
        where: { name: article.category },
        update: {},
        create: {
          name: article.category,
          slug: slugifyCategory(article.category),
        },
      });

      const post = await prisma.post.upsert({
        where: { slug: article.slug },
        update: {
          title: article.title,
          excerpt: article.excerpt,
          coverImageUrl: article.image,
          publishedAt: new Date(article.date),
          readingTime: article.readTime,
          status: "published",
          categoryId: category.id,
          seoTitle: article.title,
          seoDescription: article.excerpt,
        },
        create: {
          slug: article.slug,
          title: article.title,
          excerpt: article.excerpt,
          coverImageUrl: article.image,
          publishedAt: new Date(article.date),
          readingTime: article.readTime,
          status: "published",
          categoryId: category.id,
          seoTitle: article.title,
          seoDescription: article.excerpt,
        },
      });

      await prisma.postSection.deleteMany({ where: { postId: post.id } });
      await prisma.media.deleteMany({ where: { postId: post.id } });

      await prisma.media.create({
        data: {
          postId: post.id,
          url: article.image,
          altText: article.title,
          type: "cover",
        },
      });

      for (let i = 0; i < article.body.length; i++) {
        const sectionText = article.body[i];
        await prisma.postSection.create({
          data: {
            postId: post.id,
            heading: sectionText.slice(0, 60),
            anchor: makeAnchor(sectionText, i),
            content: sectionText,
            sortOrder: i + 1,
          },
        });
      }
    }

    console.log(
      `Seed completed successfully. Imported ${seedArticles.length} articles.`,
    );
  } finally {
    await prisma.$disconnect();
  }
}

const entryPath = process.argv[1]
